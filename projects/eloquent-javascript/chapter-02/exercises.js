function drawChessboard(num) {
  var result = []
  var str = ''
  for (let i = 0; i < num; i++){
      for (let j = 0; j <= num; j++){
        if (result.length % 2 !== 0){
          str += ' '
        }
          if (str.length % 2 === 0){
          str += '#'
        } else {
          str += ' '
        }
      }
    result.push(str +'\n')
  }
  console.log(result)
}

